## Concetnation of tuples
# Concetnation of tuples  means join two tuples using '+' operator.Concatenation of tuples is done always from the end of the original tuple

tuples1 = (1,2,3,4,5)
tuples2 = ('python', 'shakil', 'ahmed')
# Concatination tuple
tuples3 = tuples1 + tuples2
print(tuples3)

# Note- Only the same datatypes can be combined with concatenation, an error arises if a list and a tuple are combined. 




