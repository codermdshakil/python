
## Escaping single quatation
string1 = 'I\'m Shakil Ahmed'
print(string1)

## Escaping double quatation
string2 = "I'm \"Shakil\""
print(string2)

## Printing Paths with the Escaping blackslash
string3 = "c:\\python\\string\\"
print(string3)

## Printing Paths with the use of Tab
string4 = "Hi\tGeeks"
print(string4)

## Printing path with new line
string5 = "Shakil\nAhmed"
print(string5)

