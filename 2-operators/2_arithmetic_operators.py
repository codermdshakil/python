# Python Arithmetic Operators
a = 15
b = 2
# + addition
print(a + b); # 17

# - Substraction
print(a - b); #13

# * Multipication
print(a * b); #30

# / Division
print(a / b); #7.5

# ** Floor Division [just integer]
print(a // b); #7 

# % Modulas or Remainder
print(a % b); #1

# ** Exponentiation
print(2** 3); # 8 [2^3 = 2*2*2 = 8]




