# Python Comparison Operators
# == Equal
# != Not Equal
# > Greater then
# >= Greater then or Equal
# < Less then
# <= Smaller then or Equal

# Boolean value -- true or false

print(5 > 2) # True
print(4 > 7) # False
print(10 >= 10) # Equal or Greater then  [True]
print(10 <= 10) # Equal or Greater then [True]
print(10 == 10) # Equal [True]
print(10 != 10) # Not Equal [False]




